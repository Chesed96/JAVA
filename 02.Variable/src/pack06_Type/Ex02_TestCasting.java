package pack06_Type;

public class Ex02_TestCasting {
	public static void main(String[] args) {
		
		int a;
		a = 0;
		System.out.println(a);
		
		int b = 10;
		System.out.println(b);
		
		byte c = 20;
		int d = c;
		System.out.println(c);
		System.out.println(d);
		
		int e = 50;
		byte f = (byte)e;
		System.out.println(e);
		System.out.println(f);

		
	}
}
